Regels = {
    "Scissors": ["Paper", "Lizard"],
    "Paper": ["Rock", "Spock"],
    "Rock": ["Lizard", "Scissors"],
    "Lizard": ["Spock", "Paper"],
    "Spock": ["Scissors", "Rock"]
}

def determine_winner(Player_keuze, Computer_keuze):
    if Player_keuze == Computer_keuze:
        return "Het is gelijkspel!"
    elif Computer_keuze in Regels[Player_keuze]:
        return f"Je hebt gewonnen! {Player_keuze} verslaat {Computer_keuze}."
    else:
        return f"Je hebt verloren! {Computer_keuze} verslaat {Player_keuze}."

def play_game():
    choices = ["Rock", "Paper", "Scissors", "Lizard", "Spock"]
    
    while True:
        print("Kies één: Rock, Paper, Scissors, Lizard, Spock")
        Player_keuze = input("Jouw keuze: ").capitalize()

        if Player_keuze in choices:
            break  
        else:
            print("Deze keuze bestaat niet. Probeer opnieuw.")
    
    Computer_keuze = random.choice(choices)
    
    
    print(f"\nPlayer koos: {Player_keuze}")
    print(ascii_art[Player_keuze])
    print(f"Computer koos: {Computer_keuze}")
    print(ascii_art[Computer_keuze])

    
    result = determine_winner(Player_keuze, Computer_keuze)
    print(result)

play_game()
